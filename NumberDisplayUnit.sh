#!/bin/bash -x

NUMBER="100"

case "$NUMBER" in
                "1")
                echo "The Number is Unit"
                ;;
                "10")
                echo "The NUMBER is Tens"
                ;;
                "100")
                echo "The NUMBER is HUNDEREDs"
                ;;
                "1000")
                echo "The NUMBER is THOUSAND"
                ;;
esac
