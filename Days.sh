		"monday")
		echo "The day is monday"
		;;
		"tuesday")
		echo "The day is tuesday"
		;;
		"wednesday")
                echo "The day is wednesday"
		;;
		"thrusday")
                echo "The day is thrusday"
		;;
		"friday")
                echo "The day is friday"
		;;
		"satday")
                echo "The day is satday"
		;;
		"sunday")
                echo "The days is sunday"
		;;
esac
